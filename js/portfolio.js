const lightbox = document.querySelector("[data-lightbox]");

if (lightbox) {
  const titleNode = lightbox.querySelector("#lightbox-title");
  const countNode = lightbox.querySelector("[data-lightbox-count]");
  const imageNode = lightbox.querySelector("[data-lightbox-image]");
  const captionNode = lightbox.querySelector("[data-lightbox-caption]");
  const thumbsNode = lightbox.querySelector("[data-lightbox-thumbs]");
  const prevButton = lightbox.querySelector("[data-lightbox-prev]");
  const nextButton = lightbox.querySelector("[data-lightbox-next]");
  const closeButtons = lightbox.querySelectorAll("[data-lightbox-close]");
  const triggers = document.querySelectorAll("[data-gallery]");

  let activeGalleryKey = "";
  let activeIndex = 0;
  let lastTrigger = null;
  let touchStartX = 0;

  const renderImage = () => {
    const gallery = galleries[activeGalleryKey];
    if (!gallery) {
      return;
    }

    const item = gallery.items[activeIndex];
    titleNode.textContent = gallery.title;
    countNode.textContent = `${activeIndex + 1} / ${gallery.items.length}`;
    imageNode.src = item.src;
    imageNode.alt = item.alt;
    captionNode.textContent = item.alt;

    [...thumbsNode.children].forEach((thumb, index) => {
      thumb.classList.toggle("is-active", index === activeIndex);
    });
  };

  const renderThumbs = () => {
    const gallery = galleries[activeGalleryKey];
    if (!gallery) {
      thumbsNode.innerHTML = "";
      return;
    }

    thumbsNode.innerHTML = gallery.items
      .map(
        (item, index) => `
          <button class="lightbox-thumb${index === activeIndex ? " is-active" : ""}" type="button" data-lightbox-thumb="${index}" aria-label="Open image ${index + 1}">
            <img src="${item.src}" alt="${item.alt}" />
          </button>
        `
      )
      .join("");
  };

  const openLightbox = (galleryKey, trigger) => {
    if (!galleries[galleryKey]) {
      return;
    }

    activeGalleryKey = galleryKey;
    activeIndex = 0;
    lastTrigger = trigger;
    renderThumbs();
    renderImage();
    lightbox.hidden = false;
    document.body.style.overflow = "hidden";
  };

  const closeLightbox = () => {
    lightbox.hidden = true;
    document.body.style.overflow = "";
    imageNode.src = "";
    if (lastTrigger) {
      lastTrigger.focus();
    }
  };

  const goTo = (index) => {
    const gallery = galleries[activeGalleryKey];
    if (!gallery) {
      return;
    }

    activeIndex = (index + gallery.items.length) % gallery.items.length;
    renderImage();
  };

  triggers.forEach((trigger) => {
    trigger.addEventListener("click", () => {
      openLightbox(trigger.dataset.gallery, trigger);
    });
  });

  prevButton.addEventListener("click", () => goTo(activeIndex - 1));
  nextButton.addEventListener("click", () => goTo(activeIndex + 1));

  thumbsNode.addEventListener("click", (event) => {
    const button = event.target.closest("[data-lightbox-thumb]");
    if (!button) {
      return;
    }

    goTo(Number(button.dataset.lightboxThumb));
  });

  closeButtons.forEach((button) => {
    button.addEventListener("click", closeLightbox);
  });

  document.addEventListener("keydown", (event) => {
    if (lightbox.hidden) {
      return;
    }

    if (event.key === "Escape") {
      closeLightbox();
    } else if (event.key === "ArrowLeft") {
      goTo(activeIndex - 1);
    } else if (event.key === "ArrowRight") {
      goTo(activeIndex + 1);
    }
  });

  lightbox.addEventListener("touchstart", (event) => {
    touchStartX = event.changedTouches[0].clientX;
  }, { passive: true });

  lightbox.addEventListener("touchend", (event) => {
    const deltaX = event.changedTouches[0].clientX - touchStartX;
    if (Math.abs(deltaX) < 40) {
      return;
    }

    if (deltaX > 0) {
      goTo(activeIndex - 1);
    } else {
      goTo(activeIndex + 1);
    }
  }, { passive: true });
}
